n1 = int(input("Введите число от 1 до 99: "))
if 1 <= n1 <= 99:
    if 5 <= n1 % 10 <= 9 or n1 % 10 == 0 or 11 <= n1 <= 19:
        print(n1, "копеек")
    elif n1 % 10 == 1:
        print(n1, "копейка")
    elif 2 <= n1 % 10 <= 4:
        print(n1, "копейки")
else:
    print("Ошибка ввода данных")
